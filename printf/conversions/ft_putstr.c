#include "ft_printf.h"

void	ft_putstr(char *ch, int *l)
{
	int	i;

	i = 0;
	if (ch == NULL)
	{
		if (write(1, "(null)", 6) == -1)
		{
			(*l) = -1;
			return ;
		}
		(*l) = (*l) + 6;
	}
	else
	{
		while (ch[i])
		{
			if (write(1, &ch[i], 1) == -1)
			{
				(*l) = -1;
				return ;
			}
			i++;
			(*l)++;
		}
	}
}
